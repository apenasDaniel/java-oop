
public class AprendendoSwitch {
	public static void main(String[] args) {
		
		int mes = 5;
				
		switch (mes) {
		case 1: 
			System.out.println("the correct month is January");
			break;
		case 2:
			System.out.println("the correct month is february");
			break;
		case 3: 
			System.out.println("the correct month is march");
			break;
		default:
			System.out.println("invalid month");
		}
	}

}
